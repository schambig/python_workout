print(10 * 2)

# Even if one integer devides another exactly the result will be a float
print(10 / 2) # Output: 5.0
print(10 % 2) # Output: 0
print(10 / 3) # Output: 3.3333333333333335
print(10 % 3)

# Floor division: devides and rounds down to the nearest int
print(10 // 3) # Output: 3
print(2 ** 3)

# Python follows PEMDAS, and first execute normal division then floor division
print(2 ** 3 + 3 - 7 / 1 // 4) # Output: 10.0
print(2 ** 3)
print((7 / 1) // 4)

print(8 + 3 - 1)
