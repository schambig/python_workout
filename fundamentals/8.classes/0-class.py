'''
A class is a blueprint for creating objects.
Objects are instances of classes, and classes define the properties (attributes)
and behaviors (methods) that objects created from the class will have.
Classes provide a way to structure and organize code in an object-oriented programming
(OOP) paradigm.
'''

# Simple syntax to create a class
class User:  # Use CamelCase and singular for the class name
    pass

# create an instance (object) of the User class
salo = User()
print(salo)  # <__main__.User object at 0x7f0c03c838e0>, the object is of User type
