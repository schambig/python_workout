'''
Relational Operators (to compare numeric types), we get a Boolean value as result
>, <, >=, <=, ==, !=
'''
first_number = 10
second_number = 20

result = first_number > second_number
print(result)

result = first_number < second_number
print(result)

result = first_number >= second_number
print(result)

result = first_number <= second_number
print(result)

result = first_number == second_number
print(result)

result = first_number != second_number
print(result)
